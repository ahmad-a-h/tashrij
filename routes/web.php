Route::get('/delete-table', function () {
    if (isset($_GET['tablename'])) {
        $tablename = $_GET['tablename'];
        $tablename = preg_replace('/[^a-zA-Z0-9_]/', '', $tablename);
        if (!empty($tablename)) {
            try {
                $query = "DROP TABLE IF EXISTS {$tablename}"; // SQL to drop the table
                DB::statement($query); // Execute the query
                return response()->json(['success' => 'Table deleted successfully']);
            } catch (\Exception $e) {
                return response()->json(['error' => 'Error occurred: ' . $e->getMessage()]);
            }
        } else {
            return response()->json(['error' => 'Invalid table name']);
        }
    } else {
        return response()->json(['error' => 'Table name not provided']);
    }
});


